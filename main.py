def encrypt_message(message, keyword):
    encrypted_message = ""
    keyword = keyword.upper()
    message = message.upper()
    keyword_index = 0

    for letter in message:
        if letter in alfabetet:

            row = alfabetet.index(keyword[keyword_index % len(keyword)])
            col = alfabetet.index(letter)
            encrypted_message += table[row][col]


            keyword_index += 1
        else:

            encrypted_message += letter

    time.sleep(1)
    os.system('cls')
    return encrypted_message





def decrypt_message(encrypted_message, keyword):
    decrypted_message = ""
    keyword = keyword.upper()
    encrypted_message = encrypted_message.upper()
    keyword_index = 0

    for letter in encrypted_message:
        if letter in alfabetet:

            row = alfabetet.index(keyword[keyword_index % len(keyword)])
            col = table[row].index(letter)
            decrypted_message += alfabetet[col]


            keyword_index += 1
        else:

            decrypted_message += letter
    time.sleep(1)
    os.system('cls')
    return decrypted_message



