    keyword = keyword.upper()  # konvertere keyword til uppercase
    encrypted_message = encrypted_message.upper()  # konvertere den krypterede besked til uppercase
    keyword_index = 0  # Start i begyndelsen af nøgleord.

    for letter in encrypted_message:
        if letter in alfabetet:
            # Get the row and column from the Vigenère table
            row = alfabetet.index(keyword[keyword_index % len(keyword)])  # Get row based on the keyword letter
            col = table[row].index(letter)  # Find column in the row based on the encrypted letter
            decrypted_message += alfabetet[col]  # Use the column index to get the decrypted letter

            # Ryk til den næste bogstav i nøgleordet.
            keyword_index += 1
        else:
            # If the character is not in the alphabet (e.g., space, punctuation), add it directly
            decrypted_message += letter
    time.sleep(1)
    os.system('cls')
    return decrypted_message


